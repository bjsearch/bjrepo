export function getCoursesByRegion(region: string): GolfCourse[] {
  return golfCourses.filter((course) => course.region === region);
}

export function getRandomCourse(): GolfCourse {
  return golfCourses[Math.floor(Math.random() * golfCourses.length)];
}

export function getRandomCourseFromRegion(region: string): GolfCourse | undefined {
  const courses = getCoursesByRegion(region);
  return courses.length > 0
    ? courses[Math.floor(Math.random() * courses.length)]
    : undefined;
}
